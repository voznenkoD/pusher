package com.bb.domain;

/**
 * @author dmytrov
 */
public class Item {
    private String link;
    private String title;

    public Item() {
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
